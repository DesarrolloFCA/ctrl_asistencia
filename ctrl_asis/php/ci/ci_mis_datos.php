<?php
class ci_mis_datos extends toba_ci
{
	//-----------------------------------------------------------------------------------
	//---- Configuraciones --------------------------------------------------------------
	//-----------------------------------------------------------------------------------

	function conf()
	{
	}

	//-----------------------------------------------------------------------------------
	//---- formulario -------------------------------------------------------------------
	//-----------------------------------------------------------------------------------

	function conf__formulario(ctrl_asis_ei_formulario $form)
	{
		$filtro['legajo'] = $_SESSION['agente'];

		$datos = $this->dep('mapuche')->get_datos_agente($filtro);

		$form->set_titulo('Legajo: '.$_SESSION['agente']);
		$form->set_datos($datos[0]);
	}

}
?>